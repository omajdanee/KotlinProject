
fun main(args: Array<String>) {

    var StrInt : String = "12345"
    println(StrInt)

    var IntStr : Int = StrInt.toInt() // convert from string to integer //
    println(IntStr)

    /** Convert from double > integer**/
    var doubleData  = 15.6 // Data Type : Double
    println(doubleData)

    var intDataDouble : Int = doubleData.toInt() // i convert from double to integer
    print(intDataDouble)


}